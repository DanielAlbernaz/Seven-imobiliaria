        <p style="text-align: justify;">
            Todas as informações pessoais, relativas a clientes ou visitantes, entregues a MBI Imóveis serão tratadas em concordância com a Lei Geral de Proteção de Dados Pessoais (Lei nº 13.709/2018).<br>
<br>
A informação pessoal recolhida pode incluir o seu nome, e-mail e número de telefone.<br>
<br>
O presente termo permite que a MBI Imóveis use suas informações pessoais para diferentes finalidades. O e-mail e o telefone são utilizados para que um de nossos especialistas entre em contato e forneça o atendimento desejado sob o anúncio em questão. O e-mail também pode ser usado para envio de ofertas de imóveis de acordo com o perfil do cliente perante as características do imóvel.<br>
<br>
As Informações confidenciais, obtidas por qualquer meio, deverão ser tratadas como confidenciais e mantidas em sigilo, limitando-se sua divulgação e acesso aos parceiros diretamente envolvidos, e compartilhadas exclusivamente no ambiente de trabalho. A confidencialidade das informações deverá ser preservada mesmo após o encerramento do vínculo com a MBI Imóveis.<br>
<br>
A MBI Imóveis também se compromete a não vender, alugar ou repassar suas informações para terceiros. A única exceção está em casos em que essas informações forem exigidas judicialmente.<br>
<br>
Ao nos enviar estes dados entendemos que você aceita este acordo de privacidade. A MBI Imóveis reserva-se ao direito de alterar este acordo sem aviso prévio. Deste modo, recomendamos que consulte a nossa política de privacidade com regularidade de forma a estar sempre atualizado.                        </p>
